"""
listas (arrays)
son colecciones de datos/valores, bajo un unico nombre.
para acceder a esos valores se usa un indice numerico
"""
#variable normal
pelicula = "batman"
print(pelicula)

#listas
peliculas = ["batman", "spider man", "el joker"]
print(peliculas)

cantantes = list(("2pac", "eminem", "Yatra"))
print(cantantes)

years = list(range(2020, 2050))
print(years)